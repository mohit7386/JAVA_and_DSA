
//Here we are learning how to use for each loop and working of the for each loop
//for-each loop is a special loop which is used to easily traverse the elements of arrays and collection. which is used to write simple and readable code.
public class Foreachloop {
    public static void main(String[] args) {
        String[] fruits = { "Mango", "Apple", "Orange", "Pomigranate", "Guava" };
        System.out.println("Using for-each loop:-");
        for (String str : fruits) {
            System.out.println(str);
        }

    }

}
